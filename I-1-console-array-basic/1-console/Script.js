// Console commands to know

// Display a simple message
console.log('Normal message');

// Display an error message
console.error('Error message');

// Display a warning
console.warn('Warning message');
console.warn('chair');

// Display information
console.info('Information message');

// Display an array
console.table(['apple', 'pear', 'banana']);

// Group messages
console.group('Message group');
console.log('Message 1');
console.log('Message 2');
console.groupEnd();

// Measure execution time
console.time('Timer');
// Code to measure
console.timeEnd('Timer');

// Display call trace
console.trace('Call trace');

// Clear the console
console.clear();

// Count number of calls
console.count('Counter');
console.count('Counter');

// Assertions
console.assert(1 === 1, 'This assertion is true');
console.assert(1 === 2, 'This assertion is false');


// Style messages
console.log('%cStyled text', 'color: blue; font-size: 20px; font-weight: bold;');

console.table(
    persom= {
        name: 'Mld',
        age: 22,
        gender: 'male',
    }

)
console.warn('MLD')
console.error('MLD')
console.info('MLD')
